const openI18nBtns = document.querySelectorAll('.i18n__open');
const i18nDialog = document.querySelector('#i18n-dialog');
const closeI18nBtn = i18nDialog.querySelector('.i18n__close');

// i18nDialog.show();

openI18nBtns?.forEach(oBtn => {oBtn.addEventListener('click', () => {
    // i18nDialog.showModal();
    console.log('click')
    i18nDialog.show();
    document.body.style.overflow = 'hidden';
    });     
}, false);

closeI18nBtn?.addEventListener('click', () => {
    i18nDialog.close();
}, false);

i18nDialog?.addEventListener('click', ({ target })=> {
    if ((target).nodeName === 'DIALOG') {
        (target).close('dismiss');
    }
}, false);

i18nDialog?.addEventListener('close', () => {
    document.body.style.overflow = 'auto';     
}, false);